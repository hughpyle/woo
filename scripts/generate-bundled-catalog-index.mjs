import { mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";

const repoRoot = new URL("..", import.meta.url).pathname;
const catalogsRoot = join(repoRoot, "catalogs");
const output = join(repoRoot, "src", "generated", "bundled-catalogs.ts");

const manifestPaths = readdirSync(catalogsRoot)
  .map((name) => join(catalogsRoot, name, "manifest.json"))
  .filter((path) => {
    try {
      return statSync(path).isFile();
    } catch {
      return false;
    }
  })
  .sort();

const imports = manifestPaths
  .map((path, index) => {
    const fromOutput = relative(join(repoRoot, "src", "generated"), path).replaceAll("\\", "/");
    return `import manifest${index} from "${fromOutput.startsWith(".") ? fromOutput : `./${fromOutput}`}";`;
  })
  .join("\n");

const entries = manifestPaths
  .map((path, index) => {
    const fromRoot = relative(repoRoot, path).replaceAll("\\", "/");
    return `  { path: ${JSON.stringify(fromRoot)}, manifest: manifest${index} as unknown as CatalogManifest }`;
  })
  .join(",\n");

mkdirSync(dirname(output), { recursive: true });

writeFileSync(output, `// Generated by scripts/generate-bundled-catalog-index.mjs.
// Do not hand-edit. Bundled catalogs are discovered from catalogs/*/manifest.json.

import type { CatalogManifest } from "../core/catalog-installer";
${imports}

export type BundledCatalogEntry = {
  readonly path: string;
  readonly manifest: CatalogManifest;
};

export const BUNDLED_CATALOGS: readonly BundledCatalogEntry[] = [
${entries}
];
`);
